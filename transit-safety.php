<h2><b>Transit Safety Tips</b></h2>
<p>When traveling , patrons may protect themselves from becoming victims of crime by keeping alert and 
following a few proven crime prevention techniques. The following information will help you in making your 
travels safer and more enjoyable.</p>
</br>
<h3><b>Personal Safety:</b></h3></br>
- Always be alert and aware of your surroundings and the people around you.</br>
- Refrain from listening to music or wearing headphones while riding the transit system. (IPods, cell phones and 
portable radios will distract you and could make you a potential target)</br>
- Avoid talking to strangers, especially in isolated areas.</br>
- lf carrying a purse, hold it tightly and close to your body.</br>
- If carrying a wallet, keep it in a front pocket.</br>
- lf you are attacked, scream or blow a whistle in order to bring attention to your situation.</br>

<h3><b>On Trains:</b></h3></br>
- When waiting for a train, stay in a central location that is occupied by other patrons (it's true that there is 
safety in numbers).</br>
- During off hours, ride as close to the train operator as possible.</br>
- lf someone bothers you, move to another seat or car and notify the train operator.</br>
- If you are on a train, train operators can be contacted by using the intercom at each end of every train car. 
Train operators can radio messages to the Transit Police Department. The Transit Police can then be 
dispatched to any location.</br>
- lf you are on a platform or inside a station, you may use the Police Call Box to notify the Transit Police.</br>

<h3><b>On Buses:</b></h3></br>
- Try to avoid isolated bus stops.</br>
- Stand near others and in well-lighted areas, move to your bus as it arrives.</br>
- During off hours, ride as near the bus driver as possible.</br>
- lf trouble occurs, notify the bus driver. The bus driver can radio into his/her dispatcher, who can then notify 
the Transit Police Department.</br>

<h3><b>Walking To Your Car:</b></h3></br>
- Briskly and confidently walk to your vehicle.</br>
- Scan the area as you walk and be aware of the people in the area.</br>
- lf someone looks suspicious, move away from him/her. Go back to the station and notify the station agent.</br>
- Have your keys out and ready before you reach your vehicle.</br>
- Once you have reached your car, quickly check the interior for any possible intruders. - - - - lf clear, 
enter the car and quickly lock the door behind you.</br>

<h3><b>Inform Your Children:</b></h3></br>
- If your children ride the alone, please advise them not to talk to strangers.</br>
- Teach them how to contact the police, train operators, station agents and bus drivers for assistance and help.</br>

<p>The National Center For Missing & Exploited Children has published a flyer titled, "Know the Rules...When Your 
Child Is Traveling Unaccompanied by Bus or Train." The purpose of this publication is to guide parents and their 
children in a way that will help prevent negative experiences and ensure the child's safe journey from start to 
finish when traveling by bus or train.
</p></br>
<h3><b>General Safety Tips:</b></h3></br>
- If you see something suspicious or out of place, say something call the Transit Police at 617-222-1212 or notify
an our Employee: See something? Say something.</br>
- Keep your luggage with you at all times-this help us avoid unnecessary security alerts and delays.</br>
- When waiting for a train, stand near other passengers. Do not stand near the edge of the platform.
Promptly leave the platform after exiting the train.</br>
- Don't purchase an MBTA pass or tokens from people on the street. They might be counterfeit.</br>
- Always keep your jewelry and other valuables out of sight. Turn rings so that precious stones are on the palm 
side of your hand.</br>
- Keep a firm grip on your purse. However, avoid wrapping the strap around your hand or wrist.</br>
- To prevent others from knowing where and how much money you are carrying, purchase an MBTA pass.</br>
- According to Massachusetts General Law, Chapter 272, Section 43A, smoking is strictly prohibited on all MBTA 
property and vehicles.</br>
- Please take your litter and put it in a bin when you leave the system-this helps us to keep your stations, 
trains, buses and boats clean and free of debris.</br>

<h3><b>Pickpockets:</b></h3></br>
<p>Learn how to ruin a pickpocket's day, being aware of their existence and taking these simple precautions can stop 
most pickpockets.</p>

<p>Avoid crowding in area of doors.</br>
Use handbags that close tightly and carry them securely in front of your body.</br>
Carry wallets inside your coat or front pants pocket, and never in a backpack.</br>
Avoid displaying large amounts of money in public. Use small bills when purchasing tokens.</br>
If you are jostled in a crowd, be aware that a pickpocket may be responsible.</br>
Beware of loud arguments or commotion. Incidents can be staged to distract while a pocket is being picked.</br>
Do not get distracted, always be aware of your surroundings.</br></p>
<h3><b>Identity Theft:</b></h3></br>
<p>The Federal Trade Commission has published an Identity Theft Website. The website is a national resource to learn 
about the crime of identity theft.</p></br>

<h3><b>Escalator Safety Tips:</b></h3></br>
- When on escalators, please take extra care with children. If possible, hold children when riding escalator. 
When possible, use the elevator with small children, strollers, pushchairs, wheelchairs and buggies.</br>
- Please use caution when stepping on and off the escalator, especially when you are carrying luggage.</br>
- Stand to the right and hold onto the handrail.</br>
- Avoid standing near the edge of the steps.</br>
