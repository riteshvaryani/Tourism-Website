<p><b>Fenway Park</b> is a baseball park near Kenmore Square in Boston, Massachusetts. Located at 4 Yawkey
 Way, it has served as the home ballpark of the Boston Red Sox baseball club since it opened in 1912
 and is the oldest Major League Baseball stadium currently in use.
 </br>
 </br>
Because of the ballpark's age and constrained location in the dense Fenway–Kenmore neighborhood, 
the park has had many renovations and additions over the years not initially envisioned, resulting 
in unique, quirky features, including "The Triangle," "Pesky's Pole", and most notably the famous 
Green Monster in left field. The Red Sox have sold out every home game since May 15, 2003; in 2008,
 the park sold out its 456th consecutive Red Sox game, breaking a Major League Baseball record. 
 Fans who attended this game received gifts to celebrate this accomplishment. As of July 17, 2012, 
 the Red Sox have had 760 consecutive sellouts, which is the longest such streak in Major League 
 Baseball history; the club, however, includes all tickets distributed (paid or free) when 
 reporting attendance. The sellout streak is driven by the Red Sox's loyal fan base, the success
 of the Red Sox during this time (a winning record over nine seasons, during which time the team 
 achieved at least 90 wins seven times, six playoff appearances and two World Series Championships),
 and Fenway Park's low capacity: fourth lowest in seating capacity and second lowest in total of all
 MLB stadiums; it is one of the seven MLB ballparks that cannot accommodate at least 40,000 
 spectators.
 </br>
</br>
In addition to Major League Baseball, Fenway Park has been the site of many other sporting and 
cultural events, including professional football games for the Boston Redskins and the Boston 
Patriots, concerts, soccer and hockey games, political and religious campaigns.
</br>
</br>
April 20, 2012 marked Fenway Park's centennial, and the Red Sox made plans for the celebration, such
 as a distinctive commemorative logo just as they did for Fenway's 75th and 90th birthdays. On March
 7, 2012, the park was added to the National Register of Historic Places.
 </br>
