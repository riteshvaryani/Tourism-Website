The Museum of Fine Arts in Boston, Massachusetts, is one of the largest museums in the United States, attracting over one million visitors a year. It contains over 450,000 works of art, making it one of the most comprehensive collections in the Americas. It is also the 31st most visited art museum in the world, and the fifth most-visited in the United States, as of 2012.

The museum was founded in 1870 and its current location dates to 1909. In addition to its curatorial undertakings, the museum is affiliated with an art academy, the School of the Museum of Fine Arts, and a sister museum, the Nagoya/Boston Museum of Fine Arts, in Nagoya, Japan. The current director of the museum is Malcolm Rogers.

<h3>Collection and exhibits:</h3>

<b>Some highlights of the MFA's collection include:</b></br>

-Egyptian artifacts including sculptures, sarcophagi, and jewelry.</br>
-French impressionist and post-impressionist works including Paul Gauguin's Where Do We Come From? 
What Are We? Where Are We Going?, as well as works by Manet, Renoir, Degas, Monet, Van Gogh, 
Cézanne and many others.</br>
-18th and 19th century American art, including many works by John Singleton Copley, Winslow Homer 
and John Singer Sargent.</br>
-Extensive collection of Chinese painting, calligraphy and imperial Chinese art, including some of 
the most treasured paintings in Chinese history.</br>
-The largest museum collection of Japanese works under one roof in the world outside of Japan, 
including the Edward S. Morse collection of 5,000 pieces of Japanese pottery.</br>



