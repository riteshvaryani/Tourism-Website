<p>The Franklin Park Zoo is a 72-acre (29 ha) zoo located in Boston, Massachusetts. It is currently 
operated by Zoo New England, which also operates the Stone Zoo in Stoneham, Massachusetts.The zoo 
is located in the northeast portion of Franklin Park, Boston's largest park and the last component 
of the city's famed Emerald Necklace.</br>

The zoo was opened to the public in 1912, and managed by the City of Boston until 1958, when the
 Metropolitan District Commission (MDC) took control. Under the MDC's management, the zoo opened 
 several new exhibits, including Bird's World (c. 1975), the Children's Zoo (1984), and the African
 Tropical Forest (1989). In 1991, the zoo's management was handed over to the Commonwealth 
 Zoological Corporation (CZS), which also gained management of the Stone Zoo. (In July 1997, the CZS
 was renamed Zoo New England to "reflect the changing image of both zoos".) The zoo has been 
 accredited by the Association of Zoos and Aquariums (AZA) since 1990.
</br>

<h3>Exhibits</h3>

The zoo contains more than 220 species of animals and includes the following main exhibit areas.</br>
<b>The Tropical Forest</b> (known as the African Tropical Forest from 1989 to 1997), a 3-acre (1.2 ha) 
building roofed by a huge Teflon-coated cloth dome. The building mimics the animals' natural 
environment with streams, moats, faux-rock structures, hidden fences and barriers, and free-flight 
birds. The exhibit includes gorillas, bats, mandrill, crocodiles, ocelots, capybara, pottos, tapirs,
 vulture, and a pygmy hippopotamus. This exhibit was included in the zoo's 1973 master plan, and was
 originally intended to house only African tropical species; however, more South American and Asian
 tropical animals began being displayed in the Tropical Forest by the late 1990s. One gorilla named 
 Little Joe escaped his enclosure several times in 2003. He was later separated from the gorilla 
 family, but later returned in 2007. Also in 2007, the zoo's gorilla exhibit was reopened after 
 extensive renovations. As of 2011, a giant anteater resides in the former warthog enclosure.
 </br>
</br>
<b>Serengeti Crossing</b> (known as Bongo Congo from 1997 to c. 2003), a 4-acre (1.6 ha) grassland 
exhibit with ostriches, zebras, and wildebeest. The exhibit first opened in 1997, making it one of 
the first new exhibits to open at the zoo since the opening of the African Tropical Forest in 1989.
</br>
</br>
<b>Kalahari Kingdom</b> a large Africa themed area housing an old male lion named Christopher. 
Visitors can view this lion through a replica of a land rover "crashed" into the exhibit, glass, 
or from over a moat. The opening of this exhibit in 1997 marked the first time lions had been 
exhibited at the zoo since the old Lion House was closed in the early 1970s.
</br>
</br>
<b>Outback Trail</b> where visitors can view kangaroos, cockatoos, emus, and tree kangaroos. This 
exhibit opened around 1998, the same year the Butterfly Landing exhibit first opened. Additionally,
 a new seasonal budgerigar aviary was recently added to this exhibit area.
</br>
</br>
<b>Giraffe Savannah</b> opened in 1999, is a large area containing three Masai Giraffes. A herd of 
rare Grevy's Zebra can be found coexisting with them.
</br>
</br>
<b>Bird's World</b> a large building with an Orient-themed exterior containing dozens of bird 
species in four different environments: swamp, rainforest, desert, and wetlands. Outside, there is 
a large flight cage, a flamingo exhibit, and a waterfowl pond. This building originally opened in 
1912 (the same year the zoo itself opened to the public), and was renovated around the mid 1970s to
 exhibit birds in more naturalistic environments.
</br>
</br>
<b>Butterfly Landing</b> a seasonal exhibit containing over 1,000 butterflies in free flight. This 
large outdoor "tent" also has streams and a waterfall surrounded by numerous plant species. The 
exhibit first opened in 1998.
</br>
</br>
<b>The Children's Zoo</b> a three-acre complex which includes ducks, prairie dogs, and two rare 
Amur Leopards, as well as a farmyard exhibit (known as Franklin Farm). A design for this exhibit 
was published in a 1954 master plan, but the exhibit was not built until 1962, under a much modified
 design. The Children's Zoo was renovated in the early 1980s and reopened on July 1, 1984 with a 
 grant from the George Robert White Fund. Franklin Farm first opened in June 2000.
</br>
</br>
<b>Tiger Tales</b> which contains two rescued tigers named Anala (orange) and Luther (white). The 
exhibit opened in June 2006, marking the first time tigers had been exhibited at the zoo since the 
old Lion House closed in the early 1970s.
</br>
</br>
There is also an African Wild Dog exhibit, outside the Tropical Forest, that is not part of any 
major area.
