<h2><b>Emergency Instructions</b></h2>
<p>Emergencies happen when you least expect them. That's why it's important to be ready. Wherever 
you are, it helps to have a plan so that whatever happens, you'll be prepared. The information 
presented here, created through a partnership with the MBTA, the American Red Cross of Massachusetts
 Bay, the Federal Transit Administration, and the U.S. Department of Homeland Security, can help you
 do just that.
<h3>Prepare for the Unexpected</h3>

Some of the most important safety instructions to follow as you ride the MBTA can be found 
below:</br></br><hr>
<img src="1.gif" alt="image" width="40" height="40">
Please remain calm, listen to and follow instructions provided by MBTA employees. If a passenger is
 in distress, please notify the Transit Police or a uniformed MBTA employee.</br><hr>
<img src="2.gif" alt="image" width="40" height="40">
If you see smoke or suspect a fire, please notify the Transit Police or a uniformed MBTA employee. 
If you see or hear a fire alarm, please remain calm and proceed to your nearest exit.</br><hr>
<img src="3.gif" alt="image" width="40" height="40">
If you are medically qualified and able to assist, please identify yourself to the Transit Police, 
a uniformed MBTA employee or station official.</br><hr>
<img src="4.gif" alt="image" width="40" height="40">
Notify the Transit Police or a uniformed MBTA employee of any unattended packages or suspicious 
activity. Please do not move unattended packages. The Transit Police can be reached at 
(617) 222-1212.</br><hr>
<img src="5.gif" alt="image" width="40" height="40">
Take a moment to familiarize yourself with all exits. If there is a need to evacuate, we ask that 
you please use the stairs. Please remember to assist disabled or elderly passengers.</br><hr>

<h3><b>Contact Information</b></h3><br>

- Plan how your family will stay in contact if separated by an emergency.</br>
- Make sure people close to you know how to reach you and vice versa.</br>
- Write down your information on this brochure, and keep the information current. You should keep it
 with you at all times.</br>
- Designate an out-of-town friend to be the contact point for your family. In an emergency, it may 
be easier to call long distance as local phone lines may become overloaded.</br>
<h3><b>Meeting Place</b></h3></br>

- Identify a location outside your neighborhood where your family can meet if your home is affected
 or the area is evacuated.</br>
<b><h3>Alternate Modes of Travel:</h3></b></br>

- Take time now to learn alternate routes and ways to get where you need to go. Since there are 
often multiple ways to reach a destination via MBTA, visit www.mbta.com to view timetables and 
system maps.</br>
- Keep a local map with you in case you need to walk or take another form of transportation.</br>
<h3><b>Information</b></h3></br>

- If there's an emergency, check www.mbta.com for current information about service changes and 
station closings, or call (617) 222-3200, TDD (617) 222-5146.</br>
- For more information on how to prepare your family, home, and business for emergencies, visit the
 American Red Cross of Massachusetts Bay at www.bostonredcross.org and the U.S. Department of 
 Homeland Security at www.dhs.gov.</br>
