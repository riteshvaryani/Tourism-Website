The Public Garden, also known as Boston Public Garden, is a large park located in the heart of 
Boston, Massachusetts, adjacent to Boston Common.
</br>
</br>
Together with the Boston Common, these two parks form the northern terminus of the Emerald Necklace,
 a long string of parks designed by Frederick Law Olmsted. While the Common is primarily unstructure
 d open space, the Public Garden contains a lake and a large series of formal plantings that are 
 maintained by the city and others and vary from season to season.
</br>
</br>
During the warmer seasons, the 4 acres (16,000 m2) pond is usually the home of one or more swans and
 is always the site of the Swan Boats, a famous Boston tourist attraction, which began operating in 
 1877. For a small fee, tourists can sit on a boat ornamented with a white swan at the rear. The 
 boat is then pedaled around the lake by a tour guide sitting within the swan.
</br>
</br>
The current pair of swans are mute swans named Romeo and Juliet after the Shakespearian couple, 
however, it was found that both are female.
</br>
</br>
The Public Garden is rectangular in shape and is bounded on the south by Boylston Street, on the 
west by Arlington Street, and on the north by Beacon Street where it faces Beacon Hill. On its east
 side, Charles Street divides the Public Garden from the Common. The greenway connecting the Public
 Garden with the rest of the Emerald Necklace is the strip of park that runs west down the center of
 Commonwealth Avenue towards the Back Bay Fens and the Muddy River.
